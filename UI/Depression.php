<div class="sfContentBlock">
	<p class="intro">While we don’t know exactly what causes depression, a number of things are often linked to its development. Depression usually results from a combination of recent events and other longer-term or personal factors, rather than one immediate issue or event.</p> 
	<h2>Life events</h2> 
	<p>Research suggests that continuing difficulties – long-term unemployment, living in an abusive or uncaring relationship, long-term isolation or loneliness, prolonged work stress &nbsp;– are more likely to cause depression than recent life stresses. However, recent events (such as losing your job) or a combination of events can ‘trigger' depression if you’re already at risk because of previous bad experiences or personal factors.</p> 

	<h2>Personal factors</h2> 
	<ul> 
	<li> <strong>Family history</strong> –&nbsp;
	<span style="line-height: 1.69231;">Depression can run in families and some people will be at an increased genetic risk. However, having a parent or close relative with depression doesn’t mean you’ll automatically have the same experience. Life circumstances and other personal factors are still likely to have an important influence.</span></li> <li> <strong>Personality</strong> – Some people may be more at risk of depression because of their personality, particularly if they have a tendency to worry a lot, have low self-esteem, are perfectionists, are sensitive to personal criticism, or are self-critical and negative.
    </li> <li> <strong>Serious medical illness </strong>–&nbsp;
    <span style="line-height: 18.46px;">The stress and worry of coping with a serious illness can lead to depression, especially if you’re dealing with long-term management and/or chronic pain.</span></li> <li> <strong>Drug and alcohol use </strong>–&nbsp;Drug and alcohol use can both lead to and result from depression. Many people with depression also have drug and alcohol problems. Over 500,000 Americans will experience depression and a substance use disorder at the same time, at some point in their lives.<a href="https://www.beyondblue.org.au/about-us/research-projects/statistics-and-references?sec=sec-dep" target="_blank"><sup>1</sup></a></li> </ul> 
    <h2>Changes in the brain</h2> 
    <p>Although there’s been a lot of research in this complex area, there’s still much we don’t know. Depression is not simply the result of a ‘chemical imbalance’, for example because you have too much or not enough of a particular brain chemical. It’s complicated, and there are multiple causes of major depression. Factors such as genetic vulnerability, severe life stressors, substances you may take (some medications, drugs and alcohol) and medical conditions can affect the way your brain regulates your moods.</p> <p> <span style="line-height: 18.46px;">Most modern antidepressants &nbsp;have an effect on your brain’s chemical transmitters (serotonin and noradrenaline), which relay messages between brain cells – this is thought to be how medications work for more severe depression. Psychological treatment can also help you to regulate your moods.</span></p> 
    <p>
    	<span style="line-height: 18.46px;"></span>
    	<span style="line-height: 18.46px;">Effective treatment &nbsp;can stimulate the growth of new nerve cells in circuits that regulate your mood, which is thought to play a critical part in recovering from the most severe episodes of depression.</span></p> 
    <h2><span style="line-height: 18.46px;">Remember...</span></h2> 
    <p>Everyone’s different and it's often a combination of factors that can contribute to developing depression. It's important to remember that you can't always identify the cause of depression or change difficult circumstances. The most important thing is to recognise the signs and symptoms and seek support.</p>

           
