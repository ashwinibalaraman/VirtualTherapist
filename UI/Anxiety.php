<p>Anxiety is more than just feeling stressed or worried. While stress and anxious feelings are a common response to a situation where we feel under pressure, they usually pass once the stressful situation has passed, or ‘stressor’ is removed.</p>

Anxiety is when these anxious feelings don't subside – when they're ongoing and exist without any particular reason or cause. It’s a serious condition that makes it hard to cope with daily life. Everyone feels anxious from time to time, but for someone experiencing anxiety, these feelings can't be easily controlled.</p>

<strong>"There is no single cause for anxiety conditions but rather a number of risk factors that may contribute to developing an anxiety condition." </strong>

</p>

<h2>Family history of mental health conditions</h2>
<p>Some people who experience anxiety conditions may have a genetic predisposition towards anxiety and these conditions can sometimes run in a family. However, having a parent or close relative experience anxiety or other mental health condition doesn't mean you'll automatically develop anxiety. A number of other factors play a role, including personality factors, adverse life experiences and current life circumstances.&nbsp;</p>

<h2>Personality factors</font></h2>


<p>"Research suggests that people with certain personality traits are more likely to have anxiety. For example, children who are perfectionists, easily flustered, timid, inhibited, lack self-esteem or want to control everything, sometimes develop anxiety during childhood, adolescence or as adults."</p>

<h2>Ongoing stressful events</h2>

<p>Anxiety conditions may develop because of one or more stressful life events. Common pointers include:</p>

<ul>

<li>work stress</a>&nbsp;or job change</li>

<li>change in living arrangements</li>

<li>pregnancy and giving birth</li>

<li>family and relationship problems</li>

<li>major emotional shock following a stressful or traumatic event</li>

<li>death or loss of a loved one</li>

<li>verbal, sexual, physical or emotional abuse or trauma</li>

</ul>

<h2>Physical health problems</h2>

<p>Chronic physical illness can also contribute to anxiety conditions or impact on the treatment of either the anxiety or the physical illness itself. Common chronic conditions associated with anxiety conditions include:</p>

<ul>


<li>hypertension and heart disease</li>
<li>diabetes</li>
<li>asthma</li>

<p>Some physical conditions can mimic anxiety conditions, like an overactive thyroid. It can be useful to see a doctor and be assessed to determine whether there may be a medical cause for your feelings of anxiety.</p>

</ul>		

<h2>Other mental health conditions</h2>

<p> While some people may experience an anxiety condition on its own, others may experience multiple anxiety conditions, or other mental health conditions. Depression and anxiety conditions often occur together. It's important to check for and get assistance for all these conditions at the same time.</p>


<h2>Substance use</h2>

<div>Some people who experience anxiety may use alcohol or other drugs to help them manage their condition. In some cases, this may lead to people developing a substance use problem along with their anxiety condition. Alcohol and substance use can aggravate anxiety conditions particularly as the effects of the substance wear off. It's important to check for and get assistance for any substance use conditions at the same time.</div>


<h2>Remember...</h2>

<div>Everyone’s different and it's often a combination of factors that can contribute to developing an anxiety condition. It's important to remember that you can't always identify the cause of anxiety or change difficult circumstances. The most important thing is to recognise the&nbsp; signs and symptoms and seek advice and support.</div>


